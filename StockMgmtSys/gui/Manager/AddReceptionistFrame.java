    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed
        int result=isValidate();
        boolean pas=isPassValidate();
        if(result==1)
        {
            JOptionPane.showMessageDialog(null, "please fill all text!", "imcomplete!",JOptionPane.ERROR_MESSAGE);
                return;
        }
        else if(result==2)
        {
            JOptionPane.showMessageDialog(null, "Password must be atleast 4 character long!", "imcomplete!",JOptionPane.ERROR_MESSAGE);
                return;
        }
        if(!pas)
        {
            JOptionPane.showMessageDialog(null, "password does not match!", "unmatched!",JOptionPane.ERROR_MESSAGE);
                return;
        }
        LoginPojo recp=new LoginPojo();
        recp.setEmpid(jComboEmpId.getSelectedItem().toString());
        recp.setUsername(jTxtEmpName.getText());
        recp.setUserpass(jTxtPass.getText());
        recp.setUserid(jTxtUserId.getText());
        recp.setUsertype("Receptionist");
        try
        {
            boolean re=RecepDao.addReceptionist(recp);
            if(re)
            {
                JOptionPane.showMessageDialog(null, "Record added!", "Success!",JOptionPane.INFORMATION_MESSAGE);
                clearTxt();  
                loadReceptionistDeatils();
                return;
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Record not added!", "fail!",JOptionPane.INFORMATION_MESSAGE);
                    return;
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in database!", "Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }
    }//GEN-LAST:event_jBtnAddActionPerformed

