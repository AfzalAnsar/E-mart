    private int isValidate()
    {
        if(jTxtUserId.getText().isEmpty() || jTxtPass.getText().isEmpty() || jTxtRePass.getText().isEmpty())
            return 1;
        else if(jTxtPass.getText().length()<4 || jTxtRePass.getText().length()<4)
            return 2;
        else
            return 3;
    }

    private boolean isPassValidate() 
    {
        if(jTxtPass.getText().equals(jTxtRePass.getText()))
            return true;
        return false;
    }

    private void clearTxt() {
        jTxtUserId.setText("");
        jTxtPass.setText("");
        jTxtRePass.setText("");
    }
