    private void loadReceptionistDeatils()
    {
        try
        {
            receptionists = RecepDao.getAllNonRegisteredRecep();
            if(receptionists.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Not any Unregister Receptionist", "Empty",JOptionPane.INFORMATION_MESSAGE);
                jBtnAdd.setEnabled(false);
                return;
            }
            jBtnAdd.setEnabled(true);
            Set<String> keys=receptionists.keySet();
            Iterator<String> empId=keys.iterator();
            jComboEmpId.removeAllItems();
            while(empId.hasNext())
            {
                
                jComboEmpId.addItem(empId.next());
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in database!", "Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }
    }
