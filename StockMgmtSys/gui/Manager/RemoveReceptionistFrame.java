private void setAllRecepId()
    {
        try
        {
            ArrayList<String> id = UserDao.getUserId();
            if(id.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No receptionist present!");
                return;
            }
            jComboRecepId.removeAllItems();
            for(String empid:id)
            {
                jComboRecepId.addItem(empid);
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in database!", "Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }
    }
