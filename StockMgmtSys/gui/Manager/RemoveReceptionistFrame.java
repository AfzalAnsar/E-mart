    private void jBtnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRemoveActionPerformed
        try
        {
            if(jComboRecepId.getItemCount()==0)
                return;
            boolean result=RecepDao.deleteRecep(jComboRecepId.getSelectedItem().toString());
            if(!result)
            {
                JOptionPane.showMessageDialog(null, "Record not found!", "imcomplete!",JOptionPane.INFORMATION_MESSAGE);
                    return;
            }
            JOptionPane.showMessageDialog(null, "Record deleted!", "success!",JOptionPane.INFORMATION_MESSAGE);
            setAllRecepId();
            return;
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in database!", "Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }     
    }//GEN-LAST:event_jBtnRemoveActionPerformed

