    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
        boolean pas=isPassValidate();
         int result=isValidate();
          if(result==1)
        {
            JOptionPane.showMessageDialog(null, "please fill all text!", "imcomplete!",JOptionPane.ERROR_MESSAGE);
                return;
        }
          else if(result==2)
        {
            JOptionPane.showMessageDialog(null, "Password must be atleast 4 character long!", "imcomplete!",JOptionPane.ERROR_MESSAGE);
                return;
        }
        if(!pas)
        {
            JOptionPane.showMessageDialog(null, "password does not match!", "unmatched!",JOptionPane.ERROR_MESSAGE);
                return;
        }
        RecepPojo recp=new RecepPojo();
        recp.setUserid(jComboRecepId.getSelectedItem().toString());
        recp.setUsername(jTxtRecepName.getText());
        recp.setUserpass(jTxtPass.getText());
        try
        {
            boolean re=RecepDao.updateEmployee(recp);
            if(re)
            {
                JOptionPane.showMessageDialog(null, "Record updated!", "Success!",JOptionPane.INFORMATION_MESSAGE);
                setAllEmpId();
                clearTxt();
                return;
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Record not update!", "fail!",JOptionPane.INFORMATION_MESSAGE);
                clearTxt();  
                jComboRecepId.setSelectedIndex(0);
                return;
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in database!", "Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }     
    }//GEN-LAST:event_jBtnUpdateActionPerformed

