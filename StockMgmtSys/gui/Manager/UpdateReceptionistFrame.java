    private void setAllEmpId()
    {
        try
        {
            RecepIdName=RecepDao.getAllRecepIdName();
            if(RecepIdName.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No receptionist present!");
                return;
            }
            Set<String> keys = RecepIdName.keySet();
            Iterator<String> id = keys.iterator();
            jComboRecepId.removeAllItems();
            while(id.hasNext())
            {
                jComboRecepId.addItem(id.next());
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null, "Error in database Afzal!", "Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }
    }
     private int isValidate()
    {
        if(jTxtPass.getText().isEmpty() || jTxtRePass.getText().isEmpty())
            return 1;
        else if(jTxtPass.getText().length()<4 || jTxtRePass.getText().length()<4)
            return 2;
        else
            return 3;
    }
    private boolean isPassValidate() 
    {
        if(jTxtPass.getText().equals(jTxtRePass.getText()))
            return true;
        return false;
    }

    private void clearTxt() {
        jTxtPass.setText("");
        jTxtRePass.setText("");
    }
